/**
 * Convert lat/lng to SVG x/y using equirectangular projection.
 * Centers on the Americas with appropriate viewport.
 */
export function projectToSvg(
	lat: number,
	lng: number,
	width: number,
	height: number,
): { x: number; y: number } {
	// Viewport bounds covering the Americas
	const minLng = -170;
	const maxLng = -30;
	const minLat = -58;
	const maxLat = 70;

	const x = ((lng - minLng) / (maxLng - minLng)) * width;
	const y = ((maxLat - lat) / (maxLat - minLat)) * height;

	return { x, y };
}
